package bulletinBoard;

public class forComment2List {
	String id;
	String text;
	int commentNum;
	int comment2Num;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public int getCommentNum() {
		return commentNum;
	}
	public void setCommentNum(int commentNum) {
		this.commentNum = commentNum;
	}
	public int getComment2Num() {
		return comment2Num;
	}
	public void setComment2Num(int comment2Num) {
		this.comment2Num = comment2Num;
	}
}
